      public void loadAllCustomers(){
        try {
            String[] collumns = {"Id", "Name", "Salary", "Dob", "Address", "Postal Code"};
            
            DefaultTableModel dtm = new DefaultTableModel(collumns, 0){
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            
            customerTable.setModel(dtm);
            
            ArrayList<CustomerModel> customerModels = customerController.getAllCustomer();
            
            for (CustomerModel customer : customerModels) {
                Object[] rowData = {
                    customer.getCustId(),
                    customer.getTitle() + " " + customer.getName(),
                    customer.getSalary(),
                    customer.getDob(),
                    customer.getAddress()+ " " + customer.getCity(),
                    customer.getZip()
                };
                
                dtm.addRow(rowData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(CustomerView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
      
       private void searchCustomer() {
        try {
            String custId = customerTable.getValueAt(customerTable.getSelectedRow(), 0).toString();
            
            CustomerModel customerModel = customerController.searchCustomer(custId);
            
            if (customerModel != null) {
                idTxtField.setText(customerModel.getCustId());
                titleTxtField.setText(customerModel.getTitle());
                nameTxtField.setText(customerModel.getName());
                dobTxtField.setText(customerModel.getDob());
                salaryTxtField.setText(Double.toString(customerModel.getSalary()));
                addressTxtField.setText(customerModel.getAddress());
                cityTxtField.setText(customerModel.getCity());
                provinceTxtField.setText(customerModel.getProvince());
                zipTxtField.setText(customerModel.getZip());
            } else {
                JOptionPane.showMessageDialog(this, "Customer Not Found");
            }
        } catch (SQLException ex) {
            Logger.getLogger(CustomerView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }
       
