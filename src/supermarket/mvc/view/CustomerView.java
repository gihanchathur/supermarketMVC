    private void saveCustomer(){
        try {
            CustomerModel customer = new CustomerModel(idTxtField.getText(),
                    titleTxtField.getText(),
                    nameTxtField.getText(),
                    dobTxtField.getText(),
                    Double.parseDouble(salaryTxtField.getText()),
                    addressTxtField.getText(),
                    cityTxtField.getText(),
                    provinceTxtField.getText(),
                    zipTxtField.getText());
            
            String resp = customerController.saveCustomer(customer);
            JOptionPane.showMessageDialog(this, resp);
            clear();
            loadAllCustomers();
        } catch (SQLException ex) {
            Logger.getLogger(CustomerView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
